# define functions needed for app - add, sub, multiplication & division

# addition function
def add(a, b):
    answer = a + b 
    print(str(a) + " + " + str(b) + " = " + str(answer))

# subtraction function
def sub(a,b):
    answer = a - b
    print(str(a) + " - " + str(b) + " = " + str(answer))

# multiplication function
def div(a,b):
    answer = a * b
    print(str(a) + " * " + str(b) + " = " + str(answer))

# division function
def multiply(a,b):
    answer = a / b
    print(str(a) + " / " - str(b) + " = " + str(answer))

